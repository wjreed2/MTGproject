#!/usr/bin/env node
'use strict';
/**
 * Build the p4 targeted-sweep list: the existing card_semantics rows whose encodings
 * the p4 prompt rules would change. We never re-extract the whole corpus for a prompt
 * bump — we re-extract only the affected cards (extraction upserts, so re-running a
 * card simply overwrites its row + axes).
 *
 * Rules covered (docs/engine2-p3-notes.md):
 *   2b  cast/controls-class trigger payoffs missing their defining `needs`
 *   2c  token.doubler providers (p4 re-adjudicates true doublers vs typed adders)
 *   3a  chosen-color / chosen-type anthems (adaptive param at full weight)
 *
 * status='manual' rows are excluded — those are hand-tuned and already correct.
 *
 * Usage:
 *   node scripts/semantics-p4-targets.js            # writes engine2/fixtures/reruns/p4/targets.json
 *   node scripts/semantics-p4-targets.js --dry-run  # print the list, write nothing
 * Then run the sweep (subscription window applies, same as any extraction):
 *   node scripts/semantics-extract.js --cards-from-decks engine2/fixtures/reruns/p4 --run-id p4-sweep
 */

const fs = require('fs');
const path = require('path');
const mysql = require('mysql2/promise');
require('dotenv').config({ path: path.join(__dirname, '..', '.env') });

// lives under fixtures/reruns/ (gitignored) — targeted-rerun lists are generated from
// DB state, never versioned; own subdir so the runner unions only this list.
const OUT_DIR = path.join(__dirname, '..', 'engine2', 'fixtures', 'reruns', 'p4');

const QUERIES = {
  '2b class-trigger payoffs with no needs': `
    SELECT DISTINCT o.name FROM card_semantics s
    JOIN scryfall_oracle_cards o ON o.oracle_id = s.oracle_id
    WHERE s.status <> 'manual'
      AND o.oracle_text REGEXP 'you cast (a|an|your) [^.]*spell'
      AND NOT EXISTS (SELECT 1 FROM card_semantics_axes a
                      WHERE a.oracle_id = s.oracle_id AND a.kind = 'needs')`,
  '2c token.doubler providers': `
    SELECT DISTINCT o.name FROM card_semantics_axes a
    JOIN card_semantics s ON s.oracle_id = a.oracle_id
    JOIN scryfall_oracle_cards o ON o.oracle_id = a.oracle_id
    WHERE s.status <> 'manual'
      AND a.axis = 'token.doubler' AND a.kind = 'provides'`,
  '3a chosen-color/type anthems': `
    SELECT DISTINCT o.name FROM card_semantics s
    JOIN scryfall_oracle_cards o ON o.oracle_id = s.oracle_id
    WHERE s.status <> 'manual'
      AND (o.oracle_text LIKE '%choose a color%' OR o.oracle_text LIKE '%choose a creature type%'
           OR o.oracle_text LIKE '%creature type of your choice%' OR o.oracle_text LIKE '%color of your choice%')
      AND o.oracle_text REGEXP 'get \\\\+|gets \\\\+'`,
};

async function main() {
  const dryRun = process.argv.includes('--dry-run');
  const db = mysql.createPool({
    host: process.env.DB_HOST || 'localhost', port: parseInt(process.env.DB_PORT || '3306'),
    user: process.env.DB_USER || 'root', password: process.env.DB_PASS || '',
    database: process.env.DB_NAME || 'mtgproject', connectionLimit: 2, charset: 'utf8mb4',
  });
  try {
    const names = new Set();
    for (const [label, sql] of Object.entries(QUERIES)) {
      const [rows] = await db.query(sql);
      console.log(`${label}: ${rows.length}`);
      for (const r of rows) names.add(r.name);
    }
    const list = [...names].sort();
    console.log(`\n${list.length} unique targets:`);
    for (const n of list) console.log('  ' + n);
    if (dryRun) return;
    fs.mkdirSync(OUT_DIR, { recursive: true });
    fs.writeFileSync(path.join(OUT_DIR, 'targets.json'),
      JSON.stringify({ note: 'generated by scripts/semantics-p4-targets.js — do not commit', cards: list.map(name => ({ name })) }, null, 2));
    console.log(`\nwrote ${path.relative(process.cwd(), path.join(OUT_DIR, 'targets.json'))}`);
    console.log('\nnext:\n  node scripts/semantics-extract.js --cards-from-decks engine2/fixtures/reruns/p4 --run-id p4-sweep');
  } finally {
    await db.end();
  }
}

main().catch(e => { console.error(e); process.exit(1); });
